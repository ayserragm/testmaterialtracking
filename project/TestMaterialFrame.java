    private void addMaterial() {

        String materialName = mtrlname_txt.getText();
        String category = jComboBox1.getSelectedItem().toString();
        int quantity = (int) jSpinner1.getValue(); 

        if (!materialName.isEmpty()) {
            String malzeme = materialName + " - " + category + " - Quantity: " + quantity;
            materialListModel.addElement(malzeme);
            mtrlname_txt.setText("");

            Material newMaterial = new Material(0, materialName, quantity, category);
            Database.InsertMaterial(newMaterial);

            loadMaterialsFromDatabase();

        } else {
            JOptionPane.showMessageDialog(this, "Material name cannot be empty.", "Error", JOptionPane.ERROR_MESSAGE);
        }

    }

    private void loadMaterialsFromDatabase() {
        materialListModel.clear();

        ResultSet resultSet = Database.SelectMaterials(null);

        try {
            while (resultSet.next()) {
                String materialName = resultSet.getString("material_name");
                int quantity = resultSet.getInt("quantity");
                String category = resultSet.getString("category");
                int materialId = resultSet.getInt("materialId");
                String malzeme = "Material Name: " + materialName + " - Quantity: " + quantity + "-Category: " + category + "-Material Id: " + materialId;
                materialListModel.addElement(malzeme);
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }
