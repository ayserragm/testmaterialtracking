    private void remove_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_remove_btnActionPerformed
    int selectedIndex = jList1.getSelectedIndex();

    if (selectedIndex != -1) {
        String selectedItem = materialListModel.getElementAt(selectedIndex);
        System.out.println("Selected item: " + selectedItem);

        String[] parts = selectedItem.split(" - ");
        System.out.println("Parts dizisi: " + Arrays.toString(parts));

        if (parts.length > 1) {
            String materialIdPart = parts[1].substring(parts[1].indexOf("Material Id: ") + 13);
            System.out.println("Material Id: " + materialIdPart);

            try {
                ResultSet resultSet = Database.SelectMaterials(materialIdPart);
                Material material = null;

                if (resultSet.next()) {
                    int materialId = resultSet.getInt("materialId");
                    String material_name = resultSet.getString("material_name");
                    int quantity = resultSet.getInt("quantity");
                    String category = resultSet.getString("category");

                    material = new Material(materialId, material_name, quantity, category);
                }

                if (material != null) {
                    Database.DeleteMaterial(material);
                }
            } catch (SQLException ex) {
                ex.printStackTrace();
            }

            materialListModel.remove(selectedIndex);

            loadMaterialsFromDatabase();
        } else {
            System.err.println("Hata: Material Id bulunamadı.");
        }
    } else {
        JOptionPane.showMessageDialog(this, "Please select a material to remove.", "Error", JOptionPane.ERROR_MESSAGE);
    }


    }//GEN-LAST:event_remove_btnActionPerformed

